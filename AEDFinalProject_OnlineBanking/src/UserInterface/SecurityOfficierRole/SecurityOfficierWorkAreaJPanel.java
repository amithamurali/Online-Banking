    private void btnAssignToMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignToMeActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableSecurityConcerns.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Select a row", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            SecurityConcernRequest request = (SecurityConcernRequest)tableSecurityConcerns.getValueAt(selectedRow, 3);
            request.setReceiver(userAccount);
            request.setStatus(SecurityConcernRequest.PENDING);
            populateSecurityConcernTable();
            populateMySecurityConcernTable();
            JOptionPane.showMessageDialog(this, "The selected item has been assigned to you", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAssignToMeActionPerformed

    private void btnResolvedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResolvedActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableMySecurityConcerns.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Select a row", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            SecurityConcernRequest request = (SecurityConcernRequest)tableMySecurityConcerns.getValueAt(selectedRow, 3);
if(request.getStatus().equals(SecurityConcernRequest.PENDING))
{
            request.setStatus(SecurityConcernRequest.RESOLVED);
            request.setAction(txtComments.getText());
            populateSecurityConcernTable();
            populateMySecurityConcernTable();
           JOptionPane.showMessageDialog(this, "The selected item has been resolved", "Info", JOptionPane.INFORMATION_MESSAGE);
}
else
{
           JOptionPane.showMessageDialog(this, "Sorry you cant take action on these items", "Info", JOptionPane.INFORMATION_MESSAGE);
}
        }
    }//GEN-LAST:event_btnResolvedActionPerformed

