     public void populateSecurityConcernTable(){
        DefaultTableModel model = (DefaultTableModel)tableSecurityConcerns.getModel();
        
        model.setRowCount(0);       
       
        for(WorkRequest request :  organization.getWorkQueue().getWorkRequestList()){
            
            SecurityConcernRequest workRequest = (SecurityConcernRequest) request;
            if(workRequest != null)
            {
                if(workRequest.getStatus().equals(SecurityConcernRequest.OPEN))
                {
            Object[] row = new Object[5];
            row[0] = workRequest.getSender().getPerson().getPersonName();
            row[1] = workRequest.getMessage();
            row[2] = workRequest.getSecuritySeverity().getValue();            
            row[3] = workRequest;
            row[4] = workRequest.getAction();
            
            model.addRow(row);
                }
                
            }
        
        }
    }
     
     public void populateMySecurityConcernTable(){
        DefaultTableModel model = (DefaultTableModel)tableMySecurityConcerns.getModel();
        
        model.setRowCount(0);
       
        for(WorkRequest request :  organization.getWorkQueue().getWorkRequestList()){
            
            SecurityConcernRequest workRequest = (SecurityConcernRequest) request;
            if(workRequest != null)
            {
                 if(workRequest.getStatus().equals(SecurityConcernRequest.PENDING)
                         || workRequest.getStatus().equals(SecurityConcernRequest.RESOLVED)
                         || workRequest.getStatus().equals(SecurityConcernRequest.DEFERRED))
                {
            Object[] row = new Object[5];
            row[0] = workRequest.getSender().getPerson().getPersonName();
            row[1] = workRequest.getMessage();
            row[2] = workRequest.getSecuritySeverity().getValue();            
            row[3] = workRequest;
            row[4] = workRequest.getAction();
            
            model.addRow(row);
                }
                
            }
        
        }
    }
     
